// Pass two numbers and get the max of the two.
function getMaxOfTwoNumbers(a, b) {
    return a > b ? a : b;
}

console.log(getMaxOfTwoNumbers(10, 5));
console.log(getMaxOfTwoNumbers(3, 7));
console.log(getMaxOfTwoNumbers(0, 2));
console.log(getMaxOfTwoNumbers(-1, 1));
console.log(getMaxOfTwoNumbers(-10, -11));
